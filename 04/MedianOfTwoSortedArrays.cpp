double MedianOfTwoSortedArrays::findMedianSortedArrays(std::vector<int> &nums1, std::vector<int> &nums2) {
    std::vector<int> merged;
    int i = 0;
    int j = 0;

    while (i < nums1.size() && j < nums2.size()) {
        if (nums1[i] > nums2[j]) {
            merged.push_back(nums2[j]);
            j++;
        } else {
            merged.push_back(nums1[i]);
            i++;
        }
    }
    while (i < nums1.size()) {
        merged.push_back(nums1[i]);
        i++;
    }
    while (j < nums2.size()) {
        merged.push_back(nums2[j]);
        j++;
    }
    for (const auto &x: merged) {
        std::cout << x << " ";
    }
    std::cout << "" << std::endl;
    auto size = merged.size();
    if (size % 2 == 0) {
        return (static_cast<float> (merged[size / 2 - 1]) + static_cast<float> ( merged[size / 2])) /
               static_cast<float>(2);
    } else {
        return static_cast<float>(merged[size / 2]);
    }
}


